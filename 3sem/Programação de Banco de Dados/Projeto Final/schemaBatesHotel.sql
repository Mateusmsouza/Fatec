--            BTS_HORARIO_CHECKIN, BTS_ENDERECO)


CREATE TABLE BTS_BATES(
    BTS_CNPJ NUMBER(11),
    -- VERIFICAR SE DEVER√Å SER SALVO EM DATA E USAR to_char(sysdate,'HH.MI.SS') PARA EXTRAIR
    -- OU USAR CHAR
    BTS_HORARIO_CHECKIN DATE,
